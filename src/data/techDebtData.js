export const getMetrics = (items) => {
  const total = items.length;
  const open = items.filter((i) => i.status === 'Open').length;
  const inProgress = items.filter((i) => i.status === 'In Progress').length;
  const resolved = items.filter((i) => i.status === 'Resolved').length;
  const critical = items.filter((i) => i.priority === 'Critical' && i.status !== 'Resolved').length;

  const byCategory = items.reduce((acc, item) => {
    acc[item.category] = (acc[item.category] || 0) + 1;
    return acc;
  }, {});

  const byPriority = items.reduce((acc, item) => {
    acc[item.priority] = (acc[item.priority] || 0) + 1;
    return acc;
  }, {});

  return { total, open, inProgress, resolved, critical, byCategory, byPriority };
};
